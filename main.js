        let currentLang = 'ar';

        // Language Toggle Functionality
        document.getElementById('langToggle').addEventListener('click', function() {
            const newLang = currentLang === 'ar' ? 'en' : 'ar';
            toggleLanguage(newLang);
        });

        function toggleLanguage(lang) {
            currentLang = lang;
            
            // Update HTML direction and lang attribute
            document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
            document.documentElement.lang = lang;
            
            // Update language toggle button
            document.getElementById('langToggle').querySelector('span').textContent = lang === 'ar' ? 'EN' : 'AR';
            
            // Update all translatable elements
            document.querySelectorAll('[data-en]').forEach(element => {
                const key = element.getAttribute('data-en');
                if (lang === 'ar' && translations.ar[key]) {
                    element.textContent = translations.ar[key];
                } else {
                    element.textContent = key;
                }
            });

            // Update placeholder texts
            document.querySelectorAll('[data-en-placeholder]').forEach(element => {
                const key = element.getAttribute('data-en-placeholder');
                if (lang === 'ar' && translations.ar[key]) {
                    element.placeholder = translations.ar[key];
                } else {
                    element.placeholder = key;
                }
            });
        }

        // Mobile Menu Functionality
        document.getElementById('mobileMenuToggle').addEventListener('click', function() {
            document.getElementById('mobileMenu').classList.add('active');
        });

        document.getElementById('mobileMenuClose').addEventListener('click', function() {
            document.getElementById('mobileMenu').classList.remove('active');
        });

        // Close mobile menu when clicking on links
        document.querySelectorAll('.mobile-nav-links a').forEach(link => {
            link.addEventListener('click', function() {
                document.getElementById('mobileMenu').classList.remove('active');
            });
        });

        // Enhanced Animations
        document.addEventListener('DOMContentLoaded', function() {
            // Scroll animations
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('active');
                    }
                });
            }, {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            });

            // Observe elements for scroll animations
            document.querySelectorAll('.timeline-item, .skill-category, .project-card').forEach(el => {
                observer.observe(el);
            });

            // Loading bar animation
            window.addEventListener('scroll', () => {
                const loadingBar = document.getElementById('loadingBar');
                const winHeight = window.innerHeight;
                const docHeight = document.documentElement.scrollHeight;
                const scrollTop = window.pageYOffset;
                const scrollPercent = (scrollTop / (docHeight - winHeight)) * 100;
                loadingBar.style.width = scrollPercent + '%';
            });

            // Navbar scroll effect
            window.addEventListener('scroll', function() {
                const navbar = document.getElementById('navbar');
                if (window.scrollY > 50) {
                    navbar.classList.add('scrolled');
                } else {
                    navbar.classList.remove('scrolled');
                }
            });

            // Smooth scrolling for navigation links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Animate stats counter
            function animateStats() {
                const stats = document.querySelectorAll('.stat-number');
                stats.forEach(stat => {
                    const target = parseInt(stat.textContent);
                    let current = 0;
                    const increment = target / 50;
                    const timer = setInterval(() => {
                        current += increment;
                        if (current >= target) {
                            stat.textContent = target + '+';
                            clearInterval(timer);
                        } else {
                            stat.textContent = Math.floor(current) + '+';
                        }
                    }, 30);
                });
            }

            // Initialize animations
            animateStats();
        });
    